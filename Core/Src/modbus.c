//判定当前寄存器值与接收值，选择方向
uint8_t Compare_Register(int16_t *register_value,int16_t angle){
	if(*register_value == angle){
		return 0;
	}
	if(*register_value < angle){ 			  //正转
		HAL_GPIO_WritePin(GPIOC, DIR_Pin, 1);
		PwmNum = ANGLE_BASE*(angle-*register_value);   //确定旋转的角度
	}else{
		HAL_GPIO_WritePin(GPIOC, DIR_Pin, 0);
		PwmNum = ANGLE_BASE*(*register_value-angle);
	}
	HAL_GPIO_WritePin(GPIOC, ENAB_Pin, 0);    //使能
	*register_value = angle;
	HAL_TIM_PWM_Start_IT(&htim3,TIM_CHANNEL_3);//产生pwm输出
	return 1;
}
