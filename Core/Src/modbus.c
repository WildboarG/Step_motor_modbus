/* 发送数据函数，buff为发送内容，len表示发送字节数 */
void modbus_send_data(uint8_t *buff,uint8_t len)
{
	  HAL_UART_Transmit_IT(&huart1,(uint8_t*)buff,len);      //发送数据
	  while(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_TC)!=SET);		//等待发送结束
}

/* modbus服务函数 */
void modbus_service(void)
{
	uint16_t data_CRC_value;   		//从机收到的CRC校验码
	uint16_t data_len;         		//从机收到的modbus数据长度S
	uint16_t CRC_check_result; 		//从机根据收到的数据计算出来的CRC校验码
	if(USART_RX_STA&0x8000)     //串口接收完成的标志
	{
		data_len = USART_RX_STA & 0x3fff ;
		CRC_check_result = CRC16(USART_RX_BUF,data_len-2);
		data_CRC_value= USART_RX_BUF[data_len-1]<<8 | (((uint16_t)USART_RX_BUF[data_len-2])); //对于CRC的接收，先接收低位，再接收高位（组合校验码）
		if(CRC_check_result==data_CRC_value)  //校验通过
		{
			if(USART_RX_BUF[0]==code_key)  //判断从机地址
			{
				switch(USART_RX_BUF[1])   //选择功能码
				{
					case 04:     //读多个寄存器
					{
						modbus_04_function();
						break;
					}
					case 06:      //写单个寄存器
					{
						modbus_06_function();
						break;
					}
					case 16:      //写多个寄存器
					{
						modbus_16_function();
						break;
					}

				}
			}
		}
		USART_RX_STA=0;  //开始下一次接收
	}
}

/*
​ 地址 （bit） 功能码(8bit)       数据                         校验（16bit）

  主机： 01   03          00 00  00 05                     C5  CD

  从机： 01   03        0A 00 00 00 01 00 02 00 03 00 04   C5  CD
*/

void modbus_04_function(void)
{

	uint16_t i;
	uint16_t arr_start=3;   //从机回复寄存器的值是从数组的第3位开始
	uint16_t register_len;  //查询寄存器的数量
	uint16_t register_start;		 //查询寄存器的起始地址
	modbus_Tx_buff[0] = code_key;  //从机地址
	modbus_Tx_buff[1] = 04;  //功能码
	register_len = USART_RX_BUF[5]|(((uint16_t)USART_RX_BUF[4])<<8);  //查询寄存器的数量(16bit)
	modbus_Tx_buff[2] = register_len * 2;  //要发送的数据长度（8bit）
	register_start = USART_RX_BUF[3]|(((uint16_t)USART_RX_BUF[2])<<8);  //查询寄存器的起始地址（16bit）
	for(i=0 ; register_len >i ; i++,register_start++)   
	{
		modbus_Tx_buff[arr_start]=modbus_virtual_register[register_start]>>8&0xFF;// 低字节
		arr_start++;
		modbus_Tx_buff[arr_start]=modbus_virtual_register[register_start]&0xFF; // 高字节
		arr_start++;
	}
	//由于++ 所以arr_start此时走到校验位
	arr_start=CRC16(modbus_Tx_buff,register_start*2+3); //CRC校验
	modbus_Tx_buff[register_start*2+3]=(arr_start)&0xFF;
	modbus_Tx_buff[register_start*2+4]=(arr_start>>8)&0xFF;
	modbus_send_data(modbus_Tx_buff,register_start*2+5);
}
/*
主机发送： 01 06 00 00 00 06 09 C8
地址 功能码 数据内容（地址，内容） 校验
主机接收： 01 06 00 00 00 06 09 C8
*/

// 写单个寄存器
void modbus_06_function(void)
{
	uint16_t register_addr;				//主机想要修改从机的指定寄存器编号
	if(modbus_virtual_register[0] == 0){  //电机正在转动接受的值旋转
		return;
	}

	register_addr = USART_RX_BUF[3]|(((uint16_t)USART_RX_BUF[2])<<8);  //要 写入的寄存器地址
	uint16_t temp = USART_RX_BUF[5]|(((uint16_t)USART_RX_BUF[4])<<8);
	if(Compare_Register(&modbus_virtual_register[register_addr],temp)==0){
		return;
	}

	modbus_virtual_register[0] = 0;  //busy   1.空闲  0.忙碌
	modbus_Tx_buff[0]=USART_RX_BUF[0];
	modbus_Tx_buff[1]=USART_RX_BUF[1];
	modbus_Tx_buff[2]=USART_RX_BUF[2];
	modbus_Tx_buff[3]=USART_RX_BUF[3];
	modbus_Tx_buff[4]=USART_RX_BUF[4];
	modbus_Tx_buff[5]=USART_RX_BUF[5];



	register_addr=CRC16(modbus_Tx_buff,6); //CRC校验
	modbus_Tx_buff[6]=(register_addr)&0xFF;
	modbus_Tx_buff[7]=(register_addr>>8)&0xFF;

	modbus_send_data(modbus_Tx_buff,8);  //返回给主机

}

// 写多个寄存器
void modbus_16_function(void)
{

	uint16_t i;
	uint16_t register_start;	   //修改寄存器的起始地址
	uint16_t register_num;		   //修改寄存器的数量
	uint16_t CRC_check_result;  //CRC校验的结果
	modbus_Tx_buff[0]=USART_RX_BUF[0];
	modbus_Tx_buff[1]=USART_RX_BUF[1];
	modbus_Tx_buff[2]=USART_RX_BUF[2];
	modbus_Tx_buff[3]=USART_RX_BUF[3];
	modbus_Tx_buff[4]=USART_RX_BUF[4];
	modbus_Tx_buff[5]=USART_RX_BUF[5];
	CRC_check_result=CRC16(modbus_Tx_buff,6);
	modbus_Tx_buff[6]=(CRC_check_result)&0xFF;
	modbus_Tx_buff[7]=(CRC_check_result>>8)&0xFF;

	register_start = USART_RX_BUF[3]|(((uint16_t)USART_RX_BUF[2])<<8);
	register_num = USART_RX_BUF[5]|(((uint16_t)USART_RX_BUF[4])<<8);
	for( i =0;i<register_num;i++)
	{
		modbus_virtual_register[register_start+i] = USART_RX_BUF[8+i*2]|(((uint16_t)USART_RX_BUF[7+i*2])<<8);
	}
	modbus_send_data(modbus_Tx_buff,8);

}

